<br>
<b>1. How do I make a wedding cake consultation?</b>
<br>
We offer consultations on Tuesday, Wednesday and Thursday from 10am - 5pm.   Saturday appointments are available only on holiday weekends and during the winter months.  Our Saturdays are dedicated to the couples marrying on that day … we will give you the same attention on your special day.  We accept appointments by telephone only.
<br>
<br>
<b>2. What happens during a consultation?</b>
<br>
You will have the opportunity to look through our portfolios and show us what designs details appeal to you.  We also encourage you to bring in cake photos, color schemes, china patterns and whatever else you think may inspire us.  We will begin to sketch out the cake of your dreams.  And the best part, or so we hear, is the box of cake samples that you will take home to taste.
<br>
<br>
<b>3. How do I reserve my wedding date?</b>
<br>
We require a $100 non-refundable deposit.  We accept cash and check only.  This deposit will be put towards the total cost of your cake.  The final payment is due two weeks prior to your wedding date.
<br>
<br>
<b>4. Can I choose more than one flavor of cake?</b>
<br>
Absolutely!  Each tier of our cakes consist of four layers of cake and three layers of filling.  WOW!  This gives you the opportunity to offer multiple flavors to your guests as well as making a beautiful presentation once plated.  Each tier can also be a different flavor combination. 
<br>
<br>
<b>5. Why is fondant expensive?</b>
<br>
Fondant is a labor intensive means of cake decorating.  So the more time it takes to decorate your cake, the more it costs.   You don’t always have to use fondant to create the “look” you desire.  We’ll always discuss these options during your consultation.
<br>
<br>
<b>6. Do you make cupcakes for weddings?</b>
<br>
Yes.  Cupcakes are usually elaborately decorated so they are generally more expensive than a tiered wedding cake.  
<br>
<br>
<b>7. Can I add layers of styrofoam to make my cake taller?</b>
<br>
Sure.  Even though no one is going to eat these layers there is still a cost.  There is labor involved to decorate the styro to match the rest of the cake.
<br>
<br>
<b>8. Is there a delivery fee to my reception venue?</b>
<br>
Maybe.  Wedding venues in downtown Cleveland and the west suburbs do not incur a delivery charge.  Please inquire if your venue does incur a delivery charge.
<br>
<br>
<b>9. Do you make centerpiece cakes for weddings?</b>
<br>
Yes.   You generally purchase more cake than necessary to serve the number of guests at each table.  However, you offset the cost of a floral centerpiece.  This can become an interactive part of your reception as guests move around to other tables to see what flavors of cake are offered.  Each cake is showcased on a white porcelain cake stand.
<br>
<br>
<b>10.  What do you promise us?</b>
<br>
We promise you our personal attention beginning with our initial consultation and ending with set-up of the cake on your wedding day.  We promise to hand bake a yummy cake for you.  We promise to be budget conscious and offer you cost saving alternatives.  We promise to work with your reception venue and florist to coordinate delivery of your cake.  We promise to offer you the 15 years of our experience.  We promise to guide you through the wedding cake process.  We promise you a beautiful, delicious wedding cake.
